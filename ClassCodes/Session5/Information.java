public class Information <Attribute> {
    private Attribute Name;
    private Attribute Family;
    private Attribute Age;
    private Attribute Username;
    private Attribute Pass;
    private Attribute Job;
    public Attribute getName() {
        return Name;
    }

    public void setName(Attribute name) {
        Name = name;
    }

    public Attribute getFamily() {
        return Family;
    }

    public void setFamily(Attribute family) {
        Family = family;
    }

    public Attribute getAge() {
        return Age;
    }

    public void setAge(Attribute age) {
        Age = age;
    }

    public Attribute getPass() {
        return Pass;
    }

    public void setPass(Attribute pass) {
        Pass = pass;
    }

    public Attribute getUsername() {
        return Username;
    }

    public void setUsername(Attribute username) {
        Username = username;
    }

    public Attribute getJob() {
        return Job;
    }
    public void setJob(Attribute job) {
        Job = job;
    }
}
